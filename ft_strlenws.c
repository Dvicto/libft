#include "libft.h"

size_t	ft_strlenws(const char *s)
{
	size_t	i;

	i = 0;
	while (*s != '\0')
	{
		if (*s != ' ' && *s != '\n' && *s != '\t')
			i++;
		s++;
	}
	return (i);
}
