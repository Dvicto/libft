#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*ss1;
	const unsigned char	*ss2;
	size_t				i;

	i = 0;
	ss1 = (const unsigned char *)s1;
	ss2 = (const unsigned char *)s2;
	while (i < n && (*ss1 == *ss2))
	{
		ss1++;
		ss2++;
		i++;
	}
	if (i == n)
		return (0);
	return (*ss1 - *ss2);
}
